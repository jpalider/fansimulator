

public class Routing {

	/**
	 * @uml.property  name="server"
	 */
	private Server server;

	/**
	 * Getter of the property <tt>server</tt>
	 * @return  Returns the server.
	 * @uml.property  name="server"
	 */
	public Server getServer() {
		return server;
	}

	/**
	 * Setter of the property <tt>server</tt>
	 * @param server  The server to set.
	 * @uml.property  name="server"
	 */
	public void setServer(Server server) {
		this.server = server;
	}

	/**
	 * @uml.property  name="probablity"
	 */
	private float probablity;

	/**
	 * Getter of the property <tt>probablity</tt>
	 * @return  Returns the probablity.
	 * @uml.property  name="probablity"
	 */
	public float getProbablity() {
		return probablity;
	}

	/**
	 * Setter of the property <tt>probablity</tt>
	 * @param probablity  The probablity to set.
	 * @uml.property  name="probablity"
	 */
	public void setProbablity(float probablity) {
		this.probablity = probablity;
	}

}
